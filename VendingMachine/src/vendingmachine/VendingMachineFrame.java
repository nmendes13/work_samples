    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        selectedProduct = (Product)jComboBox1.getSelectedItem();
        boolean success=false;
        double insertedAmount=0;
        double exchangeAmount=0;
        Exchange exchange = new Exchange();
        if(selectedProduct != null){
            if(jTextArea2.getText().trim().length() > 0){
                if(jTextField1.getText().trim().length() > 0){
                    try
                    {
                        insertedAmount = Double.parseDouble(jTextField1.getText().trim());
                        exchangeAmount = Math.round((insertedAmount - selectedProduct.getValue()) * 100.0 ) / 100.0;;
                        
                        if(exchangeAmount >= 0){
                            if(VendingMachineLogic.HasExchange(exchangeAmount, machine)){
                                exchange = VendingMachineLogic.GiveExchange(exchangeAmount, machine);
                                success = true;
                            }else{
                                jTextArea1.append("> Sorry, there are not enought change for you\r\n");
                            }
                            
                        }else{
                            jTextArea1.append("> Please insert a higher amount to buy this product\r\n");
                        }
                    }
                    catch(NumberFormatException e)
                    {
                        jTextArea1.append("> Please insert a valid amount\r\n");
                    }
                }else{
                    jTextArea1.append("> Please insert Â£"+selectedProduct.getValue()+" to by the "+selectedProduct.getName()+"\r\n");
                }
            }else{
                jTextArea1.append("> Please select a product first"+"\r\n");
            }
        }
        else{
            jTextArea1.append("> The Machine is empty"+"\r\n");
        }
        
        
        
        if(success){
            VendingMachineLogic.BuyProduct(selectedProduct, machine);
            jTextArea1.append("> You bought the product " + selectedProduct.getName()+"!\r\n");
            
            if(exchangeAmount > 0){
                jTextArea1.append(exchange.toString());
                jTextArea1.append("> Total change: Â£"+exchangeAmount+"\r\n");
            }
            
            jComboBox1.removeItem(selectedProduct);
            selectedProduct = null;
            jTextArea2.setText("");
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

